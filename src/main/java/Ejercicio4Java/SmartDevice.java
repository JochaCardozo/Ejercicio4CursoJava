package Ejercicio4Java;

public class SmartDevice {

    //1. Atributos

    public String marca;
    public String modelo;
    public String color;
    public int RAM;
    public boolean bluthoo;
    public boolean wifi;
    public double precio;

    //2. Contructores
    public SmartDevice (){

    }

    public SmartDevice( String marca, String modelo, String color, int RAM, boolean bluthoo,
                        boolean wifi, double precio) {
        this.marca = marca;
        this.modelo = modelo;
        this.color = color;
        this.RAM = RAM;
        this.bluthoo = bluthoo;
        this.wifi = wifi;
        this.precio = precio;
    }
    }







